import { countZeros } from './countZeros';

describe('Count zeros', () => {
  const cases = [
    [0, 4],
    [1, 10],
    [1, 19],
    [3, 30],
    [9, 99],
    [11, 100],
    [13, 102],
    [29, 199],
    [31, 200],
    [43, 234],
    [161, 815],
    [189, 999],
    [192, 1000],
    [492, 2000],
    [664, 2345],
    [792, 3000],
    [2292, 8000],
    [2592, 9000],
    [2889, 9999],
    [2893, 10000],
    [6893, 20000],
    [10893, 30000],
    [14893, 40000],
    [38894, 100000],
    [488895, 1000000],
  ];
  it.each(cases)('should return %d for %d', (expected, n) => {
    expect(countZeros(n)).toBe(expected);
  });
});
